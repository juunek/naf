              <p class="mt-3">When you submit your application for Social Security Disability benefits due to a spinal cord injury, it is crucial that you understand what information the SSA must see in order to approve your case for benefits. If you are applying based on a Blue Book listing, such as one of the listings mentioned above, make sure you provide evidence showing that you meet all of the criteria that has been established in that listing.</p>

              <p>If your spinal cord injury does not meet any of the listings that have been published in the Blue Book, you will need to try to apply for disability benefits based on a vocational allowance. In order to do this, you must be able to prove to the SSA that you are unable to perform any type of work activity whatsoever. This can be done through a combination of statements from your treating physicians, medical records and a residual functional capacity report.</p>
