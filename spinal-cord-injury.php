              <h3 class="text-naf-blue my-3">Qualifying Criteria</h3>

              <p>There are a few different listings in the SSA’s Blue Book that may qualify an individual for Social Security Disability benefits due to a spinal cord injury. It is important to understand, however, that a spinal cord injury in and of itself is not enough to qualify you for benefits. You must be able to prove that your injury meets the criteria of a Blue Book listing or that you are unable to perform any type of work activity whatsoever and be approved under a vocational allowance.</p>

              <a href="https://www.ssa.gov/disability/professionals/bluebook/"  target='_blank' class="links-naf-blue">SSA'S Blue Book Listings</a>

              <p class="mt-3">Some of the Blue Book listings that may qualify a spinal cord injury for Social Security Disability benefits include:</p>

              <h4 class="my-3">Section 1.04: Disorders of the Spine</h4>

              <p>Under this Blue Book listing you can qualify for Social Security Disability benefits if you have suffered a spinal cord injury, such as a vertebral fracture, that results in a compromise of the nerve root or the spinal cord with:</p>

              <ul>

                  <li class="mb-3">Evidence of nerve root compression that is characterized by neuro-anatomic distribution of pain, limitation of motion of the spine, motor loss with sensory or reflex loss and, if there is involvement of the lower back, problems with sitting; or</li>
                  <li class="mb-3">Proof of spinal arachnoiditis that has been confirmed by an operative note or pathology report of tissue biopsy or by appropriate medically acceptable imaging that has manifested by severe burning or painful dysesthesia and results in the need for changes in position or posture more than once every 2 hours; or</li>
                  <li class="mb-3">Proof of lumbar spinal stenosis that results in pseudoclaudication and is established by findings on appropriate medically acceptable imaging and is manifested by chronic nonradicular pain and weakness and results in the inability to ambulate effectively.</li>

              </ul>

               <h4 class="my-3">Section 11.08: Spinal Cord or Nerve Root Lesions Due to Any Cause</h4>

               <p>Under this Blue Book listing you can qualify for Social Security Disability benefits if you suffer from spinal cord or nerve root lesions due to any cause, including any kind of spinal cord injury. In order to qualify for benefits and this section you must prove through medical imaging that you suffer from lesions to the spinal cord or nerve root and that they result in a disorganization of motor function.</p>
